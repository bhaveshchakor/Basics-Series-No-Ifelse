package week_2_ControlStatements_Loops;
public class WhileHello{
	public static void main(String[] args){
		int i=1; // initialization
		
		while(i<6){		// condition
			System.out.println("Hello World!");
			i++;		// increment
		}
		System.out.println("Next is decrement");
		int x=6;
		while(x>1)
			{
			System.out.println("Hello World!");
			x--;  //decrement
		}
	}
}
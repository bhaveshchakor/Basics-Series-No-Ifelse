package week_1;
public class simpleinterest
{
	public static void main(String[] args)
	{
		double p = 11151; // java automatically converts int value to double this is known as natural type casting
		int n = 3;
		int r = 10;
		double si = p*n*r/100;
		System.out.println(si);
		
		int a = 10000;
		int b = 3;
		int c = 10;
		double s = a*b*c/100;
		System.out.println(s);
	}
}